#String -
string = "HI, how have you been?"
print(type(string))

#Integer -
integer = 12345
print(type(int))

#Float -
num_2 = 12.345
print(type(num_2))

#Bool -
check = True
print(type(check))

#List -
lst = ["HI", 6, "have", "you", "been"]
print(type(lst))
print(len(lst))

#Dictionary -
dct = {'Name': "LOLO", 'Age': 99}
print(type(dct))

#Tuple -
num_3 = 12, 34, 5
print(type(num_3))

set_ex = {12, 34, 'string', 56, 12, 'string'}
print(set_ex)
print(type(set_ex))
print(13 in set_ex)

print(type(None))

