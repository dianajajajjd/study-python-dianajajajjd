#String -
string = "HI, how have you been?"
print(type(string))

#Integer -
int = 12345
print(type(int))

#Float -
num_2 = 12.345
print(type(num_2))

#Bool -
check = True
print(type(check))

#List -
lst = ["HI", "how", "have", "you", "been"]
print(type(lst))

#Dictionary -
dct = {'Name': "LOLO", 'Age': 99}
print(type(dct))

#Tuple -
num_3 = 12, 34, 5
print(type(num_3))

set_ex = {12, 34, 56, 12}
print(set_ex)
print(type(set_ex))

print(type(None))

